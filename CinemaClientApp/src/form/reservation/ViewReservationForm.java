    private void btnReservationDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservationDetailsActionPerformed
        int row = tableReservation.getSelectedRow();

        if (row != -1) {
            Reservation r = tmr.getReservation(row);
            ReservationForm rf = new ReservationForm(mf, true, FrmMode.EDIT);
            rf.populateEditForm(r, this);
            rf.setVisible(true);
            populateTable();

        } else {
            JOptionPane.showMessageDialog(this, "Please select the row for which you want to see more details.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnReservationDetailsActionPerformed

