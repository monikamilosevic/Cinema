    private void btnDeleteReservationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteReservationActionPerformed
        int row = tableReservation.getSelectedRow();

        if (row != -1) {
            Reservation r = tmr.getReservation(row);

            int points = r.getMember().getTotalScore() - r.getProjection().getScore();
            r.getMember().setTotalScore(points);

            int freeSeats = r.getProjection().getFreeSeats() + r.getTickets();

            r.getProjection().setFreeSeats(freeSeats);

            int response = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this reservation for " + r.getMember() + "? "
                    + "If you delete this reservation, member will lose all the points.", "Delete", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.OK_OPTION) {
                Response res = Controler.getInstance().deleteReservation(r);

                if (res.isStatus()) {
                    JOptionPane.showMessageDialog(this, res.getMessage(), "Succssesful", JOptionPane.INFORMATION_MESSAGE);
                    populateTable();
                } else {
                    JOptionPane.showMessageDialog(this, res.getMessage(), "Unsuccessful", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select the row for which you want to see more details.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteReservationActionPerformed


